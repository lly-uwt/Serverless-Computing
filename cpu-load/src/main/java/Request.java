
public class Request {
	private int inputChilds;

	public Request() {
	}

	public Request(int theInputChilds) {
		this.inputChilds = theInputChilds;
	}

	public void setInputChilds(int theInputChilds) {
		this.inputChilds = theInputChilds;
	}

	public int getInputChilds() {
		return this.inputChilds;
	}
}